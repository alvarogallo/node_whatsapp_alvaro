async function gracefulShutdown(signal) {
    if (isShuttingDown) {
        console.log(`\n‚ö†Ô∏è  Forzando cierre... (ya en proceso de cierre)`);
        process.exit(1);
    }
    
    isShuttingDown = true;
    console.log(`\nüì¥ Recibida se√±al ${signal}. Iniciando cierre correcto...`);
    
    try {
        // Cerrar todas las sesiones de WhatsApp activas
        const { getAllSessions, destroySession } = require('./services/whatsapp');
        const sessions = getAllSessions();
        
        if (sessions.length > 0) {
            console.log(`üîÑ Cerrando ${sessions.length} sesi√≥n(es) activa(s)...`);
            
            const closePromises = sessions.map(async (session) => {
                try {
                    console.log(`  üì¥ Cerrando sesi√≥n: ${session.sessionId}`);
                    await destroySession(session.sessionId);
                    console.log(`  ‚úÖ Sesi√≥n ${session.sessionId} cerrada correctamente`);
                } catch (error) {
                    console.error(`  ‚ùå Error cerrando sesi√≥n ${session.sessionId}:`, error.message);
                }
            });
            
            // Esperar m√°ximo 10 segundos para cerrar todas las sesiones
            await Promise.race([
                Promise.all(closePromises),
                new Promise(resolve => setTimeout(resolve, 10000))
            ]);
        }
        
        console.log('‚úÖ Todas las sesiones cerradas correctamente');
        
    } catch (error) {
        console.error('‚ùå Error durante el cierre:', error.message);
    }
    
    console.log('üëã Servidor cerrado correctamente. ¬°Hasta luego!');
    process.exit(0);
}

// Manejar Ctrl+C (SIGINT)
process.on('SIGINT', () => gracefulShutdown('SIGINT'));

// Manejar terminaci√≥n del proceso (SIGTERM)
process.on('SIGTERM', () => gracefulShutdown('SIGTERM'));

// Manejar errores no capturados
process.on('uncaughtException', (error) => {
    console.error('üí• Error no capturado:', error);
    gracefulShutdown('UNCAUGHT_EXCEPTION');
});

process.on('unhandledRejection', (reason, promise) => {
    console.error('üí• Promesa rechazada no manejada:', reason);
    gracefulShutdown('UNHANDLED_REJECTION');
});

